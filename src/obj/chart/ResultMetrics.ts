/**
  * ResultMetrics is the second version
  * of of Metrics, it is to generalise the result set
  * that would be outputted by the backend
  */


/**
  * Type infers the volumes description
  * It is currently a set that can serialised easily
  * enough
  */
export type RunResultVolumesDescription = Set<string>
export type RunResultTocksDescription = Set<string>


/**
  * RunResultSource
  * Used to understand the bit that has transferred though
  * the circuit and how it has performed
  */
export type RunResultSource = {
  [key: string]: number
}


/**
  * RunResultOptimisation
  * This is to outline the optimisation that has
  * occurred for this unit
  */
export type RunResultOptimisation = {
  [key: string]: number
}

/**
  * RunResult Tocks
  * Generally, this is what we are referring to as cycle
  * count for the results that are currently generated from
  * the backend
  */
export type RunResultTocks = {
  [key: string]: number
}


/**
  * RunResult Volumes
  * Reported by the backend to display
  * data as part of the run result chart
  */
export type RunResultVolumes = {
  [key: string]: number
}

/**
  * RunResultEntry
  * Entry that is generated by the backend
  * for most types given, this is likely to be extended
  * or change over time
  */
export type RunResultEntry = {
  tocks: RunResultTocks,
  volumes: RunResultVolumes,
  t_source: RunResultSource,
  optimisation: RunResultOptimisation,
  CACHED: boolean // Outlines if the entry has been cached or not
  result_tag: string //A tag that is associated with the entry
}


/**
  * RunResultDisplayEntry
  * A display entry is one which combines the fields of the run result
  * entry itself and has additional fields like mxid and kind
  */
export type RunResultDisplayEntry = {
  kind: string,
  status: string,
  mxid: number, //Message ID
  nqubits: number,
  volumes: RunResultVolumes,
  tSource: RunResultSource,
  tocks: RunResultTocks,
  CACHED: boolean
}


/**
  * RunResultFactory
  * Used to construct the relevant kinds
  * that will be used for displaying on the runresult chart
  * This class is mostly just static methods
  *
  */
export class RunResultFactory {

  /**
    * Returns an empty description set
    * for the volumes
    */
  static emptyVolumesDescriptionSet(): RunResultVolumesDescription {
    return new Set();
  }

  /**
    * Returns an empty description set
    * for the tocks
    */
  static emptyTocksDescriptionSet(): RunResultTocksDescription {
    return new Set();
  }
  
  /**
    * Given a particular entry
    * We are able to enumerate the the `.volumes` field and extract
    * the entries that are contained inside there
    */
  static deriveVolumesDescriptionFromResultEntry(entry: RunResultEntry):
    RunResultVolumesDescription {
    const description: Set<string> = new Set();
    for(const key in entry.volumes) {
      const keyStr = key as string;
      description.add(keyStr);
    }
    
    return description;
  }

  /**
    * Given a particular entry
    * We are able to enumerate the the `.volumes` field and extract
    * the entries that are contained inside there
    */
  static deriveTocksDescriptionFromResultEntry(entry: RunResultEntry):
    RunResultVolumesDescription {
    const description: Set<string> = new Set();
    for(const key in entry.tocks) {
      const keyStr = key as string;
      description.add(keyStr);
    }
    
    return description;
  }
  /**
    * Makes an empty volume object that
    * can be read by the run result chart
    * No Description has been supplied for this run result factory
    */
  static makeEmptyVolumesNoDescription(): RunResultVolumes {
    const emptyEntry: RunResultVolumes = {};
    return emptyEntry;
  }
  
  /**
    * Makes an empty volume object that
    * can be read by the run result chart
    * No Description has been supplied for this run result factory
    */
  static makeEmptyTocksNoDescription(): RunResultTocks {
    const emptyEntry: RunResultTocks = {};
    return emptyEntry;
  }
  
  /**
    * Makes an empty volume object that
    * can be read by the run result chart
    */
  static makeEmptyVolumes(volumeDescriptions: Set<string>): RunResultVolumes {
    const emptyEntry: RunResultVolumes = {};
    for(const volDesc of volumeDescriptions) {
      emptyEntry[volDesc] = 0;
    }
    return emptyEntry;
  }

  /**
    * Makes an empty set of tocks
    * That can be used to display set tocks in the run chart
    */
  static makeEmptyTocks(tocksDescriptions: Set<string>): RunResultTocks {
    const emptyEntry: RunResultTocks = {};
    for(const tockDesc of tocksDescriptions) {
      emptyEntry[tockDesc] = 0;
    }
    return emptyEntry;
  }

  /**
    * Given a decoded/determined result entry
    * We should be able to generate a relevant object for the runresult
    * service
    */
  static makeFromDecoded(
    entry: RunResultEntry,
    msgid: number,
    kind: string,
    status: string,
    nqubits: number): RunResultDisplayEntry {
    
    return {
      volumes: entry.volumes,
      tSource: entry.t_source,
      tocks: entry.tocks,
      CACHED: entry.CACHED,
      mxid: msgid,
      kind,
      status,
      nqubits,
    }     
  }

  /**
    * Given a decoded/determined result object
    * This object will be used to construct the total
    * Object
    */
  static makeTotalFromDecoded(jsonObj: any) {
    return {
				volumes: jsonObj.volumes,
				tSource: jsonObj.t_source,
				tocks: jsonObj.tocks,
				cuID: jsonObj.cu_id,
				status: jsonObj.status,
				npQubits: jsonObj.np_qubits
		};
  }

  /**
    * Makes an empty entry from zero
    * JSON data
    */
  static makeEmptyEntry() {
    return {
      volumes: {},
      t_source: {},
      tSource: {},
      tocks: {},
      cuID: 0,
      status: '',
      npQubits: 0,
      CACHED: false,
      result_tag: 'Invalid',
      optimisation: {},
    }
  }
}
